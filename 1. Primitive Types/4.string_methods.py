course = " Python Programming"

# capitalize the whole string
print(course.upper())
print(course.lower())

# capitalize the first letters
print(course.title())

# removes the whitespace in the string
print(course.strip())

# finds the index in the string
print(course.find("Pro"))

# replacing a sequence of characters in a string
print(course.replace("P","J"))

# checking if the series of characters exist
print("pro" in course)